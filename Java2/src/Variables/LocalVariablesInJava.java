public class LocalVariablesInJava {
	
	
	public static void m1(){		
		int a =10 ;
		int b =20;
		
		System.out.println(a+b);
	}
	
	public static void m2(){		
		int a =30 ;
		int b =40;
		System.out.println(a*b);
	}

	public static void main(String[] args) {
		m1();
		m2();
		
		

	}

}
