package Number_problems_in_java;

public class Tables {
	
	public static void main(String[] args) { // table in incremental way 


		for (int i=1;i<=10;i++) {
			
			
			System.out.println("3 " + "*" + i +" = " + + i*3);
			
		}
		
		for (int i =10 ; i>=1;i--) {
			
			
			System.out.println("3 " + "*" + i +" = " + + i*3);
			
		}
		
	}

}
